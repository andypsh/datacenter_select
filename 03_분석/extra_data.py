def tier(name: str) -> int:
    """1=광역시, 2=주요도시, 3=중소도시(시), 4=군"""
    if name in TIER1_CITIES:
        return 1
    if name in TIER2_CITIES:
        return 2
    if name.endswith("시"):
        return 3
    return 4


# 티어별 기본 점수 (0~100, 높을수록 좋음)
_TIER_POWER = {1: 88, 2: 78, 3: 65, 4: 50}
_TIER_TELECOM = {1: 95, 2: 82, 3: 65, 4: 45}
_TIER_WORKFORCE = {1: 92, 2: 75, 3: 50, 4: 30}


def power_stability(name: str) -> float:
    """전력 공급 안정성 점수 (0~100). 변전소·송전망 인프라 추정.

    실데이터 교체 시 KEPCO 변전소 위치 + KPX 송전망 여유율 사용.
    """
    base = _TIER_POWER[tier(name)]
    # 수도권 변전망 부담 보정: 서울/인천/경기 광역시급은 살짝 감점
    if REGION_PROVINCE.get(name) in {"서울", "인천", "경기"} and tier(name) <= 2:
        base -= 5
    return float(base)


def telecom_infra(name: str) -> float:
    """통신 인프라 점수 (0~100). 백본망·광케이블 보급률 추정."""
    return float(_TIER_TELECOM[tier(name)])


def it_workforce(name: str) -> float:
    """IT/공학 인력 공급 점수 (0~100). 인구 × IT 종사자 비율 추정."""
    base = _TIER_WORKFORCE[tier(name)]
    # 판교(성남)·서울·대전 등 IT 클러스터 가산
    if name in {"성남시", "서울특별시", "대전광역시", "수원시"}:
        base = min(100, base + 6)
    return float(base)


def regional_vitality(name: str) -> float:
    """지역활력 점수 (0~100). 인구감소지역 지정 = 100 (데이터센터 유치 시 정책 기여 ↑).

    14회 공모전 주제 "공공데이터로 견인하는 지역활력" 직격 지표.
    실데이터 교체 시 행안부 인구감소지수 + 통계청 GRDP 결합.
    """
    if name in POPULATION_DECLINE:
        return 100.0
    # 지정 외 군 단위는 어느 정도 활력 기여 가능
    if tier(name) == 4:
        return 55.0
    # 중소도시
    if tier(name) == 3:
        return 35.0
    # 주요 도시
    if tier(name) == 2:
        return 15.0
    # 광역시
    return 5.0


def renewable_access(name: str) -> float:
    """재생에너지 접근성 점수 (0~100). 광역도 신재생 자원 비중 기반.

    실데이터 교체 시 KEA 신재생 발전소 위치·용량 + 한전 PPA 가능성.
    """
    prov = REGION_PROVINCE.get(name, "기타")
    return RENEWABLE_BY_PROVINCE.get(prov, 30.0)


def all_indicators(name: str) -> dict[str, float]:
    """이름 → 4개 신규 지표 raw값."""
    return {
        "power_stability": power_stability(name),
        "telecom_infra": telecom_infra(name),
        "it_workforce": it_workforce(name),
        "regional_vitality": regional_vitality(name),
        "renewable_access": renewable_access(name),
    }
